package converter;

public class XMLValues {
	double Score;
	double ViewCount;
	double AnswerCount;
	double CommentCount;
	double FavoriteCount;
	String Title;
	String Body;
	String Tags;
	String Date;

	public double getScore() {
		return Score;
	}

	public void setScore(double score) {
		Score = score;
	}

	public double getViewCount() {
		return ViewCount;
	}

	public void setViewCount(double viewCount) {
		ViewCount = viewCount;
	}

	public double getAnswerCount() {
		return AnswerCount;
	}

	public void setAnswerCount(double answerCount) {
		AnswerCount = answerCount;
	}

	public double getCommentCount() {
		return CommentCount;
	}

	public void setCommentCount(double commentCount) {
		CommentCount = commentCount;
	}

	public double getFavoriteCount() {
		return FavoriteCount;
	}

	public void setFavoriteCount(double favoriteCount) {
		FavoriteCount = favoriteCount;
	}

	public String getTitle() {
		return Title;
	}

	public void setTitle(String title) {
		Title = title;
	}

	public String getBody() {
		return Body;
	}

	public void setBody(String body) {
		Body = body;
	}

	public String getTags() {
		return Tags;
	}

	public void setTags(String tags) {
		Tags = tags;
	}

	public String getDate() {
		return Date;
	}

	public void setDate(String date) {
		Date = date;
	}

}
